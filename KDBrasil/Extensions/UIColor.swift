import UIKit

extension UIColor {

    public class var greenBrazil: UIColor {
        return UIColor(red: 84/255, green: 152/255, blue: 183/255, alpha: 1)
    }

    public class var yellowBrazil: UIColor {
        return UIColor(red: 62/255, green: 140/255, blue: 67/255, alpha: 1)
    }
}
